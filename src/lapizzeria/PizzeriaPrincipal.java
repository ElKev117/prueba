private void btnMostrarListadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarListadoActionPerformed
    int i;
    modeloClientes.removeAllElements(); //limpia el modelo
    modeloValores.removeAllElements();
    for(i=0;i<arrayClientes.size(); i++)
    {
        modeloClientes.addElement(arrayClientes.get(i));
        modeloValores.addElement(arrayValores.get(i));
    }
}//GEN-LAST:event_btnMostrarListadoActionPerformed

