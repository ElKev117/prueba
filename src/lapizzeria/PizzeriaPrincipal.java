/*
Para hacer que una clase capture los eventos es necesario usar otra palabra reservada: implements,
que sirve para agregar interfaces a las clases, y que se usa también en la línea de declaración de
éstas, después de la sentencia extends (si existe); se puede implementar más de una interfaz.
Todos los programas que requieran responder al clic de un Mouse usarán la interfaz ActionListener
que nos obliga a agregar el método actionPerformed() a nuestra clase (las interfaces son clases
especiales y casi siempre exigen que se agreguen métodos específicos). Los clics capturados remitirán
el flujo del programa al método actionPerformed() que recibe como parámetro objetos de la clase ActionEvent. 
*/
private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
    int vrTamanio, vrPorcion, vrChamp, vrGuacam, vrPeper, vrQues, vrSalam, vrBebida, vrTotal;
    String datoCliente, datoValor;
    
    vrTamanio = vrPorcion = vrChamp = vrGuacam = vrPeper = vrQues = vrSalam = vrBebida = 0;
    if(rbGrande.isSelected()) {
        vrTamanio=10000;
    }
    else
    {
        if(rbMediana.isSelected()) {
            vrTamanio = 6000;
        }
        else
        {
            if(rbPequenia.isSelected()) {
                vrTamanio = 3000;
            }
        }
    }
    if(rb1.isSelected()) {
        vrPorcion = 3000; 
    }
    else
    {
        if(rb2.isSelected()) {
            vrPorcion = 6000;
        }
        else
        {
            if(rb3.isSelected()) {
                vrPorcion = 9000;
            }
        }
    }
    if(chbChampiniones.isSelected()) {
        vrChamp = 2000;
    }
    if(chbGuacamole.isSelected()) {
        vrGuacam = 2000;
    }
    if(chbPeperoni.isSelected()) {
        vrPeper = 2000;
    }
    if(chbQueso.isSelected()) {
        vrQues = 2000;
    }
    if(chbSalami.isSelected()) {
        vrSalam = 2000;
    }
    switch(cbBebidas.getSelectedIndex())
    {
        case 0:
            vrBebida = 0;
            break;
        case 1:
            vrBebida = 2000;
            break;
        case 2:
            vrBebida = 2500;
            break;
        case 3:
            vrBebida = 3500;
            break;
        case 4:
            vrBebida = 3000;
            break;
        case 5:
            vrBebida = 4000;
            break;
        default:break;
    }
    
    vrTotal = vrTamanio+vrPorcion+vrChamp+vrGuacam+vrPeper+vrQues+vrSalam+vrBebida;
    lblValor.setText(Integer.toString(vrTotal));
    datoCliente = txtNombre.getText();
    arrayClientes.add(datoCliente);
    datoValor = lblValor.getText();
    arrayValores.add(datoValor);
}//GEN-LAST:event_btnCalcularActionPerformed

